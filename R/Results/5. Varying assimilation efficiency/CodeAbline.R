scaleText <- 1.5

results <- read.csv("/Users/lmq/Documents/IC/Final Year Project/Reports/Results/5. Varying assimilation efficiency/Data5a.csv")
pdf("/Users/lmq/Documents/IC/Final Year Project/Reports/Results/5. Varying assimilation efficiency/Fig5a.pdf")
myline.fit <- lm(results[,3] ~ results[,2])
summary(myline.fit)
myline.fit2 <- lm(results[,4] ~ results[,2])
summary(myline.fit2)
plot(results[,2],results[,3],xlab="R*1:R*2 Ratio",ylab="Probability of Earlier Extinction",ylim=c(0,1.0),col="red",pch=20,cex.lab=scaleText,cex.axis=scaleText)
points(results[,2],results[,4],xlab="R*1:R*2 Ratio",col="purple",pch=22)
abline(myline.fit)
abline(myline.fit2)
points(results[,2],results[,5],xlab="R*1:R*2 Ratio",col="green",pch=7)
legend("topright",c("Pred 1","Pred 2","Neither extinct"),col=c("Red","Purple","green"),pch=c(20,22,7),cex=0.9*scaleText)
dev.off()

